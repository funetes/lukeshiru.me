/**
 * Link interface.
 * @author Lucas Ciruzzi <hello@lukeshiru.me>
 * @export
 * @interface Link
 */
export interface Link {
	/**
	 * Link color.
	 * @type {string}
	 */
	color: string;
	/**
	 * Link location.
	 * @type {string}
	 */
	href: string;
	/**
	 * Link icon.
	 * @type {string}
	 */
	icon: string;
	/**
	 * Link name.
	 * @type {string}
	 */
	name: string;
}
